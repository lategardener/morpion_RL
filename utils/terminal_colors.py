# ANSI escape sequences for colored and formatted terminal output

RED = "\033[31m"      # Red text color
BLUE = "\033[34m"     # Blue text color
GREEN = "\033[32m"    # Green text color
YELLOW = "\033[33m"   # Yellow text color
WHITE = "\033[37m"    # White text color

FAINT = "\033[2m"     # Faint (dim) text effect
RESET = "\033[0m"     # Reset all text formatting to default

BOLD = "\033[1m"      # Bold text effect
